# Получить строку: Принять на вход строку, которую нужно проверить.
# Привести строку к единому регистру: Для корректного сравнения игнорировать регистр (сделать все символы строчными).
# Удалить все пробелы и лишние символы: Оставить только буквы и цифры (можно проигнорировать знаки пунктуации).
# Сравнить строку с её перевернутой версией: Если строка и её обратная версия совпадают, то это палиндром.
# Вернуть результат: Вернуть True, если строка является палиндромом, иначе False.


import re

def is_palindrome(s):
    # 1. Привести строку к нижнему регистру
    s = s.lower()

    # 2. Удалить все символы, кроме букв и цифр
    s = re.sub(r'[^a-z0-9]', '', s)

    # 3. Сравнить строку с её перевернутой версией
    return s == s[::-1]

print(is_palindrome("A man, a plan, a canal: Panama"))  # True
print(is_palindrome("race a car"))  # False
